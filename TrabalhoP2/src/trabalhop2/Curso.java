package trabalhop2;

class Curso {
	private String nome; 
    private String curso; 
    private double nota;
    
    public Curso(String nome, double nota) {
        this.curso = curso;       
    }

    public String getcurso() {
        return curso;
    }

	public void setcurso(String nome) {
		this.curso = nome;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCurso() {
		return curso;
	}

	public void setCurso(String curso) {
		this.curso = curso;
	}

	public double getNota() {
		return nota;
	}

	
	public void setNota(double nota) {
		this.nota = nota;
	}
    
       
}